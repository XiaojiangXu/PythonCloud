# -*- coding:utf-8 -*-
class count:
    
    def __init__(self,a,b):
        self.a = int(a)
        self.b = int(b)
        
    def add(self):
        return self.a+self.b
    
    def subtract(self):
        return self.a-self.b
        
    def multiply(self):
        return self.a*self.b
    
    def divide(self):
        return self.a/self.b